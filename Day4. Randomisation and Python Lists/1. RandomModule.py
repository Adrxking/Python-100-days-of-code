import random

# Obtener numeros enteros random entre 1 y 10
random_integer = random.randint(1, 10)
print(random_integer)

# Obtener numeros decimales random entre 0 y 1
random_float = random.random()
print(random_float)

# Obtener numeros decimales random entre 0 y 5
random_float = random_float*5
print(random_float)

